package Day08;

public class Calculator {
	
	// field
	// static + final : 프로그램 전반적으로 사용되는 상수
	static final double pi = 3.14159;
	// static : 객체 없이 프로그램 전반적으로 사용되는 상수
	static double pi2 = 3.14;
	// final : 객체 내에서 사용되는 상수
	
	// constructor
	
	// method
	int plus (int x, int y) {
		return x + y;
	}
	
	int minus (int x, int y) {
		return x - y;
	}

}
