package Day01; //패키지명 [소스파일들의 폴더]

public class Day01_1 { // class start
	// public : 접근제한자
		// class : 클래스 선언
			// Day01_1 : 클래스 이름
	// * 모든 곳에서 사용하는 Day01_1 이라는 이름을 갖는 클래스 선언
	// { : 시작 }: 끝
	
	// 1. class {} 안에서 코드 작성한다. [class 밖에서 작성 불가]
	// 2. 실행코드 main
		// main 입력 후 ctrl + spacebar -> enter
	public static void main(String[] args) {
		// public : 접근제한자
			// static : 정적
				// void : 반환타입[돌려주는 값]이 없다
					// main : 스레드(코드를 읽어주는 흐름)
		// * main함수{} 안에 작성된 코드는 실행된다.
			// syso ctrl + spacebar
		System.out.println("java 출력"); // 고급 언어 [사람이 이해할 수 있는 언어]
		// 컴파일 [ctrl + F11]
	} // main end
	
} // class end